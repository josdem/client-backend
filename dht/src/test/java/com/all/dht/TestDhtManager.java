	@Test
	public void shouldStartAndBootstrapWithAnExistingUltrapeerAndStoreRouteTableToDisaster() throws Exception {
		when(dht.isRunning()).thenReturn(false, true);
		when(dht.isBootstrapped()).thenReturn(false, true);
		when(dhtSettings.isAnnounceRequired()).thenReturn(true);
		List<DhtNode> dhtNodes = new ArrayList<DhtNode>();
		DhtNode sameNode = new DhtNode("127.0.0.1", 0);
		DhtNode otherNode = new DhtNode("127.0.0.1", 1);
		dhtNodes.add(otherNode);
		dhtNodes.add(sameNode);
		when(dhtDiscovery.getDhtNodesList()).thenReturn(dhtNodes);
		when(dhtSettings.getDhtNode()).thenReturn(sameNode);
		when(dhtSettings.isPersistRouteTableEnabled()).thenReturn(true);

		dhtManager.start();

		verify(dht).bind(any(SocketAddress.class));
		verify(dht).start();
		verify(bootstrapper).addBootstrapHost(any(SocketAddress.class));
		verify(dhtDiscovery).getDhtNodesList();
		verify(dhtDiscovery).subscribeDhtNode(sameNode);
		verify(dispatcher).start();
		verify(dhtFileUtils).storeRouteTable(anyString(), any(RouteTable.class));
	}

