	public String more(String kuid) {
		Set<? extends DHTValueEntity> entities = get(KUID.createWithHexString(kuid));
		if (entities != null && !entities.isEmpty()) {
			StringBuilder sb = new StringBuilder("\nDhtValueInfo:\n");
			sb.append("Primary Key : ");
			sb.append(kuid);
			sb.append("\nBusiness Key : ");
			sb.append(storedKeys.get(kuid) != null ? storedKeys.get(kuid) : "Not Available");
			sb.append("\nValues:\n\n");
			for (DHTValueEntity valueEntity : entities) {
				sb.append("Value : ");
				Serializable value = null;
				try {
					value = new String(valueEntity.getValue().getValue());
				} catch (Exception e) {
					value = "ERROR: Could not deserialize a value from dht";
				}
				sb.append(value);
				sb.append("\nCreator :");
				sb.append(valueEntity.getCreator().getContactAddress());
				sb.append("\t");
				sb.append(valueEntity.getCreator().getNodeID());
				sb.append("\t");
				sb.append(new Date(valueEntity.getCreationTime()));
				sb.append("\n\n");
			}
			return sb.toString();
		} else {
			return "Error: No Matching value found for key " + kuid;
		}
	}

	public Map<String, String> getLocalKeys() {
		HashMap<String, String> localKeys = new HashMap<String, String>();
		for (DHTValueEntity entity : dht.getDatabase().values()) {
			if (isLocalValue(entity)) {
				String kuid = entity.getPrimaryKey().toHexString();
				String value = storedKeys.get(kuid);
				localKeys.put(kuid, value != null ? value : "Not available");
			}
		}
		return localKeys;
	}

	public Set<String> getAllKeys() {
		Set<String> allKeys = new TreeSet<String>();
		for (KUID kuid : dht.getDatabase().keySet()) {
			allKeys.add(kuid.toHexString());
		}
		return allKeys;
	}

	public String getNodeId() {
		return dht.getLocalNodeID().toHexString();
	}

